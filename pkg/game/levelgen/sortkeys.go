package levelgen

import "sort"

// SortedStrings returns map keys in lexicographic order for deterministic iteration.
func SortedStrings(m map[string]struct{}) []string {
	if len(m) == 0 {
		return nil
	}
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

// SortedRoomMapKeys returns sorted keys for room-keyed maps.
func SortedRoomMapKeys[V any](m map[string]V) []string {
	if len(m) == 0 {
		return nil
	}
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}
